class FloatAndDouble{
	public static void main(String[] args)
	{
		float MyMinFloatValue = Float.MIN_VALUE;
		float MyMaxFloatValue = Float.MAX_VALUE;
		System.out.println("Minimum value for float is : " + MyMinFloatValue);
		System.out.println("maximum value for float is : " + MyMaxFloatValue);
		
		double MyMindoubleValue = Double.MIN_VALUE;
		double MyMaxDoubleValue = Double.MAX_VALUE;
		System.out.println("Minimum value for double is : " + MyMindoubleValue);
		System.out.println("maximum value for double is : " + MyMaxDoubleValue);
		
	}
}